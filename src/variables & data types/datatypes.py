# There are several built-in datatypes in Python, including:
#
# Numbers: used to store numerical values, such as integers (e.g. 1, 2, 3) and floating-point numbers (e.g. 3.14).
#
# Strings: used to store text, represented by either single or double quotes (e.g. "hello" or 'world').
#
# Lists: used to store a collection of items, represented by square brackets
# (e.g. [1, 2, 3]). Lists are mutable, meaning the items in the list can be changed.
#
# Tuples: similar to lists, but are immutable, meaning the items in the tuple cannot be changed.
# Represented by parentheses (e.g. (1, 2, 3)).
#
# Dictionaries: used to store key-value pairs, represented by curly braces (e.g. {'name': 'John', 'age': 30}).
#
# Sets: used to store a collection of unique items, represented by curly braces (e.g. {1, 2, 3}).
#
# Booleans: used to store true or false values (e.g. True or False).

# Additionally, Python also supports complex numbers, represented by the complex datatype.
# A complex number is written in the form of x + yj, where x and y are real numbers and j represents the square root of -1.
#
# Python also supports a special datatype called None, which represents the absence of a value or a null value.
#
# It's also worth noting that Python is a dynamically-typed language, which means that the datatype of a variable can change during runtime.
# For example, a variable that is initially assigned an integer value can later be reassigned a string value.
#
# It is also possible to define custom datatype using classes and objects in Python,
# which allow you to create your own datatype with specific properties and methods.
#
# In summary, Python has a wide range of built-in datatype and support for custom data types,
# which provides flexibility and power to the developer. Understanding the different datatype and when to use them is an
# essential part of programming in Python.